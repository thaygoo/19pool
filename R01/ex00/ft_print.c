#include "rush01.h"

void	ft_print(int **box, int size)
{
	int		i;
	int		j;
	char	tmp;

	i = 1;
	j = 1;
	while (j < (size + 1))
	{
		i = 1;
		while (i < size + 1)
		{
			tmp = box[i][j] + 48;
			write(1, &tmp, 1);
			i++;
		}
		write(1, "\n", 1);
		j++;
	}
}
